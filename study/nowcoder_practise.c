#include<stdio.h>
int main()
{
    int n = 0;
    int m = 0;
    scanf("%d%d", &n, &m);
    long long int i = 0;
    long long int j = 0;
    if (n > m)
    {
        i = m;
        j = n;
    }
    else
    {
        i = n;
        j = m;
    }
    while (1)
    {
        if (n % i == 0 && m % i == 0)
            break;
        i--;
    }
    while (1)
    {
        if (j % n == 0 && j % m == 0)
            break;
        j++;
    }
    printf("%lld", i + j);
    return 0;
}